package day05_ArithmeticOperators;

public class ArithmeticalOperatorsPractice {

    public static void main(String[] args) {

        int num = 7;

        boolean isDividedBy3 = num%3==0;
        boolean isDividedBy5 = num%5==0;

        System.out.println(num + " is divided by 3: " + isDividedBy3);
        System.out.println(num + " is divided by 5: " + isDividedBy5);

    }
}
